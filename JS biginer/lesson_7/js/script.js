'use strict';


//Все условия начинаются с if
//Дальше мы открываем круглые скобки и пишем условия которые хотим проверить
//В фигруных скобках мы пишем, что должно случится, если условие выполнено (верно)
//Условие в круглых скобках должно быть преобразовано к Булеву типу

if (4 == 3) {
	console.log('ok!');
}


//В условие можно добавить что выполнится, если первое условие не выполняется(не верно)
// else
if (4 == 3) {
	console.log('Ok!');
} else {
	console.log('Error');
}


//Можно создать условие, которое будет выполняться всегда, просто поместив букву ввиде строки или любоую цифру
//т.е. если есть какое-то содержимое то проверка пройдена
//но если поместить ноль или пустую строку то проверка пройдена не будет.

if ('5') {
	console.log('Ok!');
} else {
	console.log('Error');
}


//Множественные условия
