//switch - нужен, чтобы избежать большого ветвления условий - это модификация if
//switch всегда проверяет на строгое соответствие

const num = 50;

switch (num) {
	case 49:
		console.log('Неверно');   // указываем то, что хотим проверить (на строгое соответствие)
		break;
	case 100:
		console.log('Неверно');   	// указываем то, что хотим проверить (на строгое соответствие)
		break;
	case 50:
		console.log('Правда');  	// указываем то, что хотим проверить (на строгое соответствие)
		break;
	default: 							 // значение по умолчанию
		console.log('не в этот раз');
		break;
}
//break - ключевое слово, которое заканчивает выполнение кода, если одно из условий было выполнено
//это обязательная синтаксическая конструкция